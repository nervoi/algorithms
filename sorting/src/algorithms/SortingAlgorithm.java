package algorithms;

public interface SortingAlgorithm {

    /**
     * Sorts an array of Integers using the selected algorithm.
     * @param arr integer array to be sorted
     * */
    void sort(int[] arr);

    /**
     * Sorts an array of Integers using the selected algorithm.
     * @param arr long array to be sorted
     * */
    void sort(long[] arr);

    /**
     * Sorts an array of Integers using the selected algorithm.
     * @param arr double array to be sorted
     * */
    void sort(double[] arr);

    /**
     * Sorts an array of Integers using the selected algorithm.
     * @param arr float array to be sorted
     * */
    void sort(float[] arr);

}
